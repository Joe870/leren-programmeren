thistuple = ("maandag", "dinsdag", "woensdag", "donderdag", "vrijdag", "zaterdag", "zondag")
#alle dagen van de week
print(thistuple)
#alle werkdagen van de week
print(thistuple[:5])
#alle weekenddagen van de week
print(thistuple[5:])
#alle dagen in omgekeerde volgorde
print(thistuple[::-1])
#alle werkdagen in omgekeerde volgorde
print(thistuple[-3::-1])
#alle weekenddagen in omgekeerde volgorde
print(thistuple[:4:-1])
